package modulo_datas;

import java.time.Duration;
import java.time.Instant;

public class InstantJTM {
	
	public static void main(String[] args) throws InterruptedException {
		
		//calculo do tempo de processos, para avaliar os tempos do processos 
		Instant inicio = Instant.now();
		
		Thread.sleep(2000);
		
		Instant ifinal = Instant.now();
		
		Duration duracao = Duration.between(inicio, ifinal);
		
		System.out.println(" Duração em nano segundos : " + duracao.toNanos());
		
		System.out.println(" Duração em minutos : " + duracao.toMinutes());
		
		System.out.println(" Duração em horas : " + duracao.toHoursPart());
		
		System.out.println(" Duração em milisegundos : " + duracao.toMillis());
		
		System.out.println(" Duração em dias : " + duracao.toDays());
	}

}
